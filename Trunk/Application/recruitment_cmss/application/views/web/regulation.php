						Regulations 
						</h3>
					</div>
					<div class=" row article-div notice-text" style="height: auto;margin-top: 1%;">		
					  <table class="table table-responsive" cellpadding="10" cellspacing="0" style="border-collapse: collapse "style="text-align: justify; font-size:17px" width="720"  id="AutoNumber4" bordercolorlight="#99CCFF" bordercolordark="#99CCFF">
						<tr>
						
						<td>
						 
				       <ol type="1" class="body-txt" style="margin-right: 10px;"> 
				      		<b style="text-aligh:center;">	As per the “the Arunachal Pradesh Staff Selection Board Rules, 2018 (Act No. 14 of 2018)”:</b>
							<li><b style="font-size:17px"> Headquarter of the Board:</b>
								 <ol type="a" class="body-txt" style="margin-right: 10px;font-size:17px">
									<li>The Headquarter of the Arunachal Pradesh Staff Selection Board constituted under section 3 of the Act shall be at Itanagar;</li>
									
								</ol>
							</li>
							<li><b style="font-size:17px"> Salaries and Allowances:-</b>
								 <ol type="a" class="body-txt" style="margin-right: 10px;font-size:17px">The Salaries, Allowances and other conditions of, -
									<li>The Chairman shall be the same as those of officers of Indian Administrative Service in the pay not below that of Super-time scale; </li>
									<li>Other Members shall be the same as those of IAS Selection Grade or equivalent;</li>
									<li>The Secretary shall be the same as those of IAS senior grade or Joint Secretary in the State Government;</li>
									<li>Other employees of the board shall be the same as those of State Government employees in the equivalent rank and grade ;  </li>
 								</ol>
							</li>
							<li><b style="font-size:17px"> Requisition of Post(s):-</b>
								<ol type="1" class="body-txt" style="margin-right: 10px;font-size:17px">
									<li>The Department shall submit the requisition for posts in the format specified by the Board in Annexure - ’C’; </li>
									<li>The Heads of Department/Appointing Authorities shall every year before 31st January intimate the Board indicating the number of vacancies including those anticipated except the post anticipate for new creation in prescribed requisition format in course of the years  ;</li>
									<li>The vacancies furnished by the Requisitioning Authority for direct recruitment shall include the Sports quota but exclude quota for Compassionate Appointment and promotion;  </li>
									<li>The Board shall frame suitable regulation for conduct of Recruitment under Sports quota.  </li>
									<li>On receipt of intimations of vacancies under rule 5, the Secretary in consultation with the Chairman and Board shall prepare an annual examination calendar and publish in the month of March every year. They shall issue advertisement in local newspapers and other Medias inviting applications from eligible candidates for appearing in the competitive examination;  </li>
								<h4>Note: The application for competitive examination may be in such form as may be decided by the Board.</h4> 
								</ol>
							</li>
							<li><b style="font-size:17px"> Eligibility of the candidates:-</b><br>
							<ol type="1"class="body-txt" style="margin-right: 10px;font-size:17px;">
							<li>A candidate in order to be eligible for appearing at the competitive examination must be satisfied to the following conditions, namely:-</li>
							</ol>
								 <ol type="a" class="body-txt" style="margin-right: 10px;font-size:17px">
									<li> He/she shall be a citizen of India.;</li>
									<li> He/she shall possess requisite qualification prescribed for the posts/services in the Recruitment Rules for which examination is being conducted; </li>
									<li> His/her age shall not be less than the minimum age limit or upper entry age limit as prescribed under the prescribed Recruitment Rules or the Arunachal Civil Services and Civil posts (upper age limit for Direct Recruitment Rules), 2014 as amended from time to time on the last date fixed for receipt of applications;</li>
									<b>Provided that the maximum age limit shall be relaxed to the categories and persons as prescribed under the Arunachal Civil Services and Civil posts (upper age limit for Direct Recruitment Rules), 2014 as amended from time to time.</b>
								</ol>
							</li>
							<li><b style="font-size:17px">Examination fees:</b>
							
								 <ol type="a" class="body-txt" style="margin-right: 10px;font-size:17px">
									<li> The examination fee for group ‘C’ post shall be determined by the Board with prior approval of the Government.;</li>
									<li> No application for admission to any examination shall be considered unless it is accompanied by an IPO/Treasury Challan/Bank Draft/Online proof of Payment showing the payment in Government Treasury or Banks payable at Itanagar; </li>
									<li> No claim for refund of fees shall be entertained in any circumstances;</li>
									<li> The Board shall decide the rates of remuneration payable to different examiners/staff/officers/Invigilators engaged in connection with the examination duties;</li>
									<li> No application shall be accepted after the closing date of submission of application;</li>
									<li> The final decision regarding acceptance/rejection of applications for any category of post shall be vested with the Board;</li>
								</ol>
							</li> 
							<li><b style="font-size:17px"> Superintendence and control of Examination</b>
								 <ol type="a" class="body-txt" style="margin-right: 10px;font-size:17px">
									<li>The Board shall be responsible for the Superintendence and conduct of the examination;</li>
								</ol>
							</li>
							<li><b style="font-size:17px">Conduct of Examination in District</b>
								 <ol type="a" class="body-txt" style="margin-right: 10px;font-size:17px">
									<li>In case of all district level posts, at least one examination Centre shall be set up in that district;</li>
								</ol>
							</li>
							<li><b style="font-size:17px">Competitive Examination: </b>
								 <ol type="a" class="body-txt" style="margin-right: 10px;font-size:17px">
									<li>Competitive Examination shall be held at a centre or centers to be announced by the Board considering the number of candidates appearing for the said examination;</li>
									<li>The Board may fix up such centers at the District or sub-divisional Headquarters, if necessary in consultation with the Deputy Commissioner/District Superintendent. The date, time and place for the examination shall be intimated at least one month before the examination;</li>
								</ol>
							</li>
							<li><b style="font-size:17px">Physical Test and Trade Test</b>
								 <ol type="a" class="body-txt" style="margin-right: 10px;font-size:17px">
									<li>The Board shall conduct physical efficiency test and trade test of the posts where such test is prescribed in the recruitment rule or mandatory for selection of candidates against a particular post. The test will be done as per the predetermined standards exist in the recruitment rule or the rule notified by the department in respect of such categories of post(s) with the aids and help of the agencies/department. The Board shall constitute a separate committee including experts from other Department and representatives of the Department for conduct of such test;</li>
								</ol>
							</li>
							<li><b style="font-size:17px">  Penalty: </b>
								 <ol type="I" class="body-txt" style="margin-right: 10px;font-size:17px">
									<li>Obtaining support of his candidature by any means; or;</li>
									<li>Impersonation; or</li>
									<li>Submitting fabricated documents or documents which have been tampered with; or</li>
									<li>Resorting to any other irregular or improper means in connection with candidature for examination; or</li>
									<li>Making statement which are incorrect or false or suppressing with candidature for the examination; or</li>
									<li> Adopting unfair means during the examination: or</li>
									<li>Writing obscene language or pornographic matter in the scripts; or</li>
									<li> Misbehaving with fellow examinee or the invigilators in any manner in the examination hall; or</li>
									<li>Harassing or causing any bodily harm to the staff employed/engaged by the Board for the conduct of examination hall, premise; or</li>
									<li>Violating any of the instructions contained in the admission certificate; or</li>
									<li><strong>Attempting to commit or, as the case may be, abetting the Board of all or any of the acts specified in forgoing clauses shall be liable-</strong>
										<ol type="a" class="body-txt" style="margin-right: 10px;font-size:17px">
											<li>To be disqualified by Board from the examination for which he is candidate; or</li>
											<li>To be debarred, either permanently or for a specified period-</li>
												<ol type="I" class="body-txt" style="margin-right: 10px;font-size:17px">
												<li>By the Board, from appearing in any examination or selection held by them; or</li>
												<li>By the State Government, from entering to any employment under them on the basis of the recommendation of the Board.</li>
												</ol>
											<li>If he is already in service under the Government, to disciplinary action under the appropriate rules;</li>
											<b>Provided that no penalty under this rule shall be imposed except;</b>
												<ol type="I" class="body-txt" style="margin-right: 10px;font-size:17px">
												<li>Giving the candidate an opportunity of making such representation to the Board or Government, as the case may be, in writing as he may wish to make in that behalf ; and</li>
												<li>Taking into consideration the representation, if any, submitted by the candidate within the period allowed to him by the Board or the Government, as the case may be.</li>
												</ol>
										</ol>
									   
									</li>  
									    
									    
								</ol>
							</li>
							<li><b style="font-size:17px">Selection of candidate:</b><p style="font-size:17px">The following guidelines shall be followed in conducting recruitment to various posts and services by the Board, namely:-</p>
								 <ol type="a" class="body-txt" style="margin-right: 10px;font-size:17px">
									<li>Answer script shall be evaluated centrally by the Board.;</li>
									<li>If two or more candidates have obtained equal marks in aggregate, the order of merit shall be determined according to their age in seniority and if that is also same, then it will be determined in accordance with the marks secured by the candidates in their respective minimum academic qualification prescribed for the posts as per Recruitment Rule;</li>
									<li>Any other standards and syllabus prescribed by the Government, if any, for any particular post/service from time to time shall be adopted;</li>
								</ol>
							</li>
							<li><b style="font-size:17px"> Select list and validity:</b>
								 <ol type="a" class="body-txt" style="margin-right: 10px;font-size:17px">
									<li>The Board shall prepare a select list of such numbers of successful candidates in order of merit on the basis of the total aggregate marks obtained in the Recruitment Examination, which shall be equal to the number of vacancies for which requisitions have been made;</li>
									<li>The select list shall be published [official website of the Board/notice Board and two local dailies] in the form of notification under intimation to the appropriate authority. The select list of the candidate forwarded against the requisition to the Appointing Authority shall be as per the merit obtained by the candidate in the recruitment examination and the option exercised by the candidate in their application in the context of combined competitive examination. The Board shall allot the post and the department to the candidate(s) selected with due consideration of merit and option. The allotment is within the power of the Board and cannot be complained against the allotment;</li>
									<li>The select list of each recruitment examination shall be valid for one year from the date of its publication or till the publication of result of the next recruitment examination whichever is earlier.;</li>
									<li>A Reserved list or Wait list shall be prepared which would contain names of candidates equal to 10% of the total vacancy or 2 names of candidates whichever is higher in order of merit, starting from the first candidate after the last name of the select list. In case, candidate selected do not become available for appointment, within the stipulated time allowed for joining the post or where a candidate joins but resigns or dies within the period of 6 (six) months from date of his joining, such vacancy would be filled up from wait list.;</li>
								</ol>
							</li>
							<li><b style="font-size:17px"> Submission of Annual Report and Laying of Annual Report:</b>
								 <ol type="a" class="body-txt" style="margin-right: 10px;font-size:17px">
								 <li>The Board shall prepare an annual report in the form, prescribed in Annexure “A” and submit to the State Government through the Department of Administrative Reforms in the end of each year that shall cause to lay the report before the Legislative Assembly. The report shall contain the details of examination, revenue collection, expenditure, details of staff members etc in format prescribed in Annexure “B”.;</li>
								 </ol>
							</li>
							<li><b style="font-size:17px">Power to Relax</b>
								<ol type="a" class="body-txt" style="margin-right: 10px;font-size:17px">
								<li>Where the Government is of the opinion that it is necessary or expedient to do so; it may, by order, for the reasons to be recorded in writing, and in consultation with the Board, relax any provisions of the rules with respect to any class or category of persons or posts.;</li>
								</ol>
							</li>
							<li><b style="font-size:17px">Power to Amend Rules: </b>
							    <ol type="a" class="body-txt" style="margin-right: 10px;font-size:17px">
							    <li>The State Government shall have the powers to alter, modify, amend, rescind or supersede these rules from time to time as may be deemed expedient.</li>
							    </ol>
								 
							</li>
						</ol>
