				       <ol type="i" class="body-txt" style="margin-right: 10px;">
							<li><b>Duties and functions of the Board</b>
								 <ol type="a" class="body-txt" style="margin-right: 10px;">
									<li>Notwithstanding anything contained in any other law for the time being in force it shall be the duty of the Board to conduct examinations and select candidates for appointments to the subordinate services/posts.</li>
									<li>It shall be the duty of the Board to perform such other functions and duties as the Government may, by notification, specify. </li>
									<li>It shall be the duty of the Board to conduct departmental examinations and advice the Government in such matters and on such other matters as may be referred to it by the Government. </li>
									<li>Notwithstanding anything contained in any other law for the time being in force, or rules, regulations, bye laws framed there under, relating to the appointment to the services/posts in, and conditions of service of employees of, the Government Departments, organizations and autonomous bodies established/ constituted by the Government and Semi-Government Organizations, the Board shall be the authority competent to conduct examinations for appointments to the subordinate services/posts of such Government Departments, organizations and autonomous bodies and Semi-Government Organizations, and it shall be the duty of the Board to conduct such examinations.</li>
									<li>On all matters relating to methods of recruitment to, or any other matter relating to, the subordinate services/posts it shall be the duty of the Board to advise on any matters so referred to by the Government Department, an organization or autonomous body established/ constituted by the Government, or a Semi-Government Organization</li>
									<li>The Government may, however, make orders specifying the matters in which either generally or in any particular class of cases or in any particular circumstances, it shall not be necessary for the Board to be consulted.</li>
									<li>In the case of any difference of opinion between the Board and the Government Department, or an organization or autonomous body established/constituted by the Government, or the Semi-Government Organizations, or on any other matter, the concerned Government Department, or an organization or autonomous body, or the Semi Government organization, shall refer the matter to the Department of Administrative Reforms, Government of Arunachal Pradesh and the decision of the Government thereon shall be final.</li>

								</ol>
							</li>
							<li><b>Duties and functions of the Chairman.</b>
								 <ol type="a" class="body-txt" style="margin-right: 10px;">
									<li>ascertaining well in advance but not less than one year, from the departments, the number of posts for which recruitment is to be made, from time to time; </li>
									<li>inviting applications by means of advertisements;</li>
									<li>scrutinizing applications received in response to advertisements;</li>
									<li>responsibility for engaging confidential printer and for getting question papers prepared ;  </li>
									<li>conducting of competitive examination for the candidates ;</li>
									<li>selection and preparation of merit list of the successful candidates ;</li>
									<li>recommending to the departments concerned names of selected candidates ; </li>
									<li>maintaining records of selections by the Board ;</li>
									<li>any other duty, or duties, entrusted to the Board by the Government, from time to time.</li>
								</ol>
							</li>
							<li><b>Duties and functions of the Members</b>
								<ol class="body-txt" style="margin-right: 10px;">
									<li> The members shall assist the Chairman in conduct of the examinations and perform other such duties as directed or delegated by the Chairman.
									</li>
								</ol>
							</li>
							<li><b>Duties and functions of the Controller of Examinations :</b>
								 <ol type="a" class="body-txt" style="margin-right: 10px;">
									<li> the actual conduct of the examination process excluding the preparation and printing of the question papers;</li>
									<li> the safe custody of examination materials, both before and after the examination ; </li>
									<li> the preparation, confidentiality, accuracy and publication of the results ;</li>
									<li> recommend and ensure the process of upgradation and reform of examination process subject to the approval of the Board ;</li>
									<li> Maintaining of records of all examinations conducted by the Board. </li>
								</ol>
							</li>
